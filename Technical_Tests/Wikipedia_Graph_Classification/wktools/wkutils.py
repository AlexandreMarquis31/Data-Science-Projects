import re

# regex that match html tags
html_regex = re.compile(r'(<!--.*?-->|<[^>]*>)')
# regex that match non-alphabetical characters (except '-')
non_alphabetical_regex = re.compile(r'([^a-zA-Z-]+)')
# stopwords from NLTK (common words that will be ignored)
stopwords = {'i', 'me', 'my', 'myself', 'we', 'our', 'ours', 'ourselves', 'you', "you're", "you've", "you'll", "you'd", 'your', 'yours', 'yourself', 'yourselves', 'he', 'him', 'his', 'himself', 'she', "she's", 'her', 'hers', 'herself', 'it', "it's", 'its', 'itself', 'they', 'them', 'their', 'theirs', 'themselves', 'what', 'which', 'who', 'whom', 'this', 'that', "that'll", 'these', 'those', 'am', 'is', 'are', 'was', 'were', 'be', 'been', 'being', 'have', 'has', 'had', 'having', 'do', 'does', 'did', 'doing', 'a', 'an', 'the', 'and', 'but', 'if', 'or', 'because', 'as', 'until', 'while', 'of', 'at', 'by', 'for', 'with', 'about', 'against', 'between', 'into', 'through', 'during', 'before', 'after', 'above', 'below', 'to', 'from', 'up', 'down', 'in', 'out', 'on', 'off', 'over', 'under', 'again', 'further', 'then', 'once', 'here', 'there', 'when', 'where', 'why', 'how', 'all', 'any', 'both', 'each', 'few', 'more', 'most', 'other', 'some', 'such', 'no', 'nor', 'not', 'only', 'own', 'same', 'so', 'than', 'too', 'very', 's', 't', 'can', 'will', 'just', 'don', "don't", 'should', "should've", 'now', 'd', 'll', 'm', 'o', 're', 've', 'y', 'ain', 'aren', "aren't", 'couldn', "couldn't", 'didn', "didn't", 'doesn', "doesn't", 'hadn', "hadn't", 'hasn', "hasn't", 'haven', "haven't", 'isn', "isn't", 'ma', 'mightn', "mightn't", 'mustn', "mustn't", 'needn', "needn't", 'shan', "shan't", 'shouldn', "shouldn't", 'wasn', "wasn't", 'weren', "weren't", 'won', "won't", 'wouldn', "wouldn't", '', 'also'}


def get_tokens(content):
    """
    Process Wikipedia's page source code (remove HTML, stopwords, small words, ... and lowercase every words)

    Parameters
    ----------
    content : str
        Wikipedia page's source code

    Returns
    -------
    tokens : list of str
        List tokenized words
    """
    raw_content = content
    content = html_regex.sub(' ', raw_content)
    content = non_alphabetical_regex.sub(' ', content)
    lower_content = content.lower()
    list_tokens = lower_content.split(' ')
    tokens = [word for word in list_tokens if word not in stopwords and len(word) > 1]
    return tokens
